package exercise05;

/*字符串操作：
字符串拼接：使用+操作符或StringBuilder类
 */
public class StringExample01 {
    public static void main(String[] args) {
        String str1 = "Hello, ";
        String str2 = "world!";
        String concatenated = str1 + str2;
        System.out.println(concatenated); //Hello, world!
    }
}


