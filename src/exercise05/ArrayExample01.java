package exercise05;

//以下是Java中数组和字符串操作的示例代码：
//数组操作：

import java.util.Arrays;

//排序数组：使用Arrays类的sort方法。
public class ArrayExample01 {
    public static void main(String[] args) {
        int[] array = {5, 3, 2, 1, 4};
        Arrays.sort(array);
        System.out.println(Arrays.toString(array)); // 输出: [1, 2, 3, 4, 5]
    }
}





